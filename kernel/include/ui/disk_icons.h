/* =========================== ICON RENDERING =========================== */

/*
 * Blend color with background for anti-aliasing effect
 */
static inline uint32_t blend_color(uint32_t color, uint32_t bg, int alpha) {
    int r = (((color >> 16) & 0xFF) * alpha + ((bg >> 16) & 0xFF) * (255 - alpha)) / 255;
    int g = (((color >> 8)  & 0xFF) * alpha + ((bg >> 8)  & 0xFF) * (255 - alpha)) / 255;
    int b = ((color         & 0xFF) * alpha + (bg         & 0xFF) * (255 - alpha)) / 255;
    return (r << 16) | (g << 8) | b;
}

/*
 * Draw a 16x16 icon at position with color.
 * The 0-bits are transparent (background is not overwritten).
 */
static inline void draw_icon_1bpp(volatile uint32_t *fb, uint32_t fb_pitch,
                                  uint32_t x, uint32_t y, 
                                  const uint8_t *icon, uint32_t color) {
    for (int row = 0; row < ICON_HEIGHT; row++) {
        /* Combine the two bytes into a 16-bit row */
        uint16_t bits = (icon[row * 2] << 8) | icon[row * 2 + 1];
        for (int col = 0; col < ICON_WIDTH; col++) {
            /* Check if the current pixel bit is set (1) */
            if (bits & (0x8000 >> col)) {
                /* Calculate framebuffer index. Assuming 32-bit (4-byte) pixels. */
                fb[(y + row) * (fb_pitch / 4) + (x + col)] = color;
            }
        }
    }
}

/*
 * Scale icon 2x with anti-aliasing (32x32 output)
 * Adds subtle edge softening for smoother appearance
 */
static inline void draw_icon_2x(volatile uint32_t *fb, uint32_t fb_pitch,
                                uint32_t x, uint32_t y,
                                const uint8_t *icon, uint32_t color) {
    /* Semi-transparent edge color for anti-aliasing */
    uint32_t bg = 0x00281830;  /* Purple background */
    uint32_t edge = blend_color(color, bg, 128);  /* 50% blend for edges */
    
    for (int row = 0; row < ICON_HEIGHT; row++) {
        uint16_t bits = (icon[row * 2] << 8) | icon[row * 2 + 1];
        uint16_t prev_bits = (row > 0) ? ((icon[(row-1) * 2] << 8) | icon[(row-1) * 2 + 1]) : 0;
        uint16_t next_bits = (row < ICON_HEIGHT-1) ? ((icon[(row+1) * 2] << 8) | icon[(row+1) * 2 + 1]) : 0;
        
        for (int col = 0; col < ICON_WIDTH; col++) {
            int bit_mask = 0x8000 >> col;
            int is_set = bits & bit_mask;
            
            if (is_set) {
                uint32_t px = x + col * 2;
                uint32_t py = y + row * 2;
                
                /* Draw main 2x2 block with full color */
                fb[py * (fb_pitch / 4) + px] = color;
                fb[py * (fb_pitch / 4) + px + 1] = color;
                fb[(py + 1) * (fb_pitch / 4) + px] = color;
                fb[(py + 1) * (fb_pitch / 4) + px + 1] = color;
            } else {
                /* Check if this is an edge pixel (adjacent to set pixel) */
                int is_edge = 0;
                
                /* Check horizontal neighbors */
                if (col > 0 && (bits & (0x8000 >> (col - 1)))) is_edge = 1;
                if (col < ICON_WIDTH-1 && (bits & (0x8000 >> (col + 1)))) is_edge = 1;
                
                /* Check vertical neighbors */
                if (prev_bits & bit_mask) is_edge = 1;
                if (next_bits & bit_mask) is_edge = 1;
                
                /* Draw subtle edge pixels for anti-aliasing */
                if (is_edge) {
                    uint32_t px = x + col * 2;
                    uint32_t py = y + row * 2;
                    fb[py * (fb_pitch / 4) + px] = edge;
                    fb[py * (fb_pitch / 4) + px + 1] = edge;
                    fb[(py + 1) * (fb_pitch / 4) + px] = edge;
                    fb[(py + 1) * (fb_pitch / 4) + px + 1] = edge;
                }
            }
        }
    }
}

/*
 * Draw icon with glow effect for better visibility
 */
static inline void draw_icon_glow(volatile uint32_t *fb, uint32_t fb_pitch,
                                  uint32_t x, uint32_t y,
                                  const uint8_t *icon, uint32_t color) {
    /* Draw subtle glow behind icon */
    uint32_t glow_color = blend_color(color, 0x00000000, 64);
    draw_icon_2x(fb, fb_pitch, x - 1, y - 1, icon, glow_color);
    draw_icon_2x(fb, fb_pitch, x + 1, y + 1, icon, glow_color);
    
    /* Draw main icon on top */
    draw_icon_2x(fb, fb_pitch, x, y, icon, color);
}

