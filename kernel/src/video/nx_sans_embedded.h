/* Get NX Sans glyph (returns 16-bit rows for 9 pixels width) */
static inline const uint16_t* nx_font_get_glyph(char c) {
    if (c < NX_FONT_FIRST || c > NX_FONT_LAST) {
        return nx_sans_glyphs[0];  /* Space for invalid */
    }
    return nx_sans_glyphs[c - NX_FONT_FIRST];
}

