struct nx_font {
    uint32_t size;
    uint32_t char_width;
    uint32_t char_height;
};

nx_font_t* nxgfx_font_default(nx_context_t* ctx, uint32_t size) {
    (void)ctx;
    nx_font_t* font = (nx_font_t*)malloc(sizeof(nx_font_t));
    if (!font) return NULL;
    font->size = size;
    font->char_width = 8 * (size / 16 + 1);
    font->char_height = 16 * (size / 16 + 1);
    return font;
}

nx_font_t* nxgfx_font_load(nx_context_t* ctx, const char* path, uint32_t size) {
    (void)path;
    return nxgfx_font_default(ctx, size);
}

void nxgfx_font_destroy(nx_font_t* font) {
    if (font) free(font);
}

void nxgfx_draw_text(nx_context_t* ctx, const char* text, nx_point_t pos, nx_font_t* font, nx_color_t color) {
    if (!ctx || !text || !font) return;
    uint32_t scale = font->size / 16 + 1;
    int32_t x = pos.x, y = pos.y;
    while (*text) {
        uint8_t c = (uint8_t)*text++;
        if (c < 32 || c > 127) c = '?';
        const uint8_t* glyph = BUILTIN_FONT_8X16[c - 32];
        for (uint32_t row = 0; row < 16; row++) {
            for (uint32_t col = 0; col < 8; col++) {
                if (glyph[row] & (0x80 >> col)) {
                    for (uint32_t sy = 0; sy < scale; sy++)
                        for (uint32_t sx = 0; sx < scale; sx++)
                            nxgfx_put_pixel(ctx, x + col*scale + sx, y + row*scale + sy, color);
                }
            }
        }
        x += 8 * scale;
    }
}

nx_size_t nxgfx_measure_text(nx_context_t* ctx, const char* text, nx_font_t* font) {
    (void)ctx;
    if (!text || !font) return (nx_size_t){0, 0};
    uint32_t scale = font->size / 16 + 1;
    size_t len = 0;
    while (text[len]) len++;
    return (nx_size_t){ (uint32_t)(len * 8 * scale), 16 * scale };
}
